import { drawTableAuraGlow, drawTableSheen, drawTableCardGlow } from './tableEffects.js'
import { createTableRectHelpers } from './tableRects.js'

export function drawTable(ctx, phase, tx, ty, auraAlpha = 0) {
  const { b, p } = createTableRectHelpers(ctx, tx, ty)
  drawTableAuraGlow(ctx, tx, ty, auraAlpha)
  b(0, 6, 32, 8, '#0a0812')
  b(0, 6, 32, 1, '#2a2040')
  b(0, 13, 32, 1, '#0e0c1c')
  b(1, 7, 30, 1, '#181228')
  b(6, 7, 8, 1, '#1e1838')
  b(18, 7, 6, 1, '#1a1430')
  b(0, 6, 1, 8, '#484060'); b(31, 6, 1, 8, '#484060')
  p(0, 6, '#8878a8'); p(31, 6, '#8878a8')
  p(0, 13, '#585078'); p(31, 13, '#585078')
  drawTableSheen(ctx, tx, ty)
  b(10, 14, 2, 3, '#141020'); b(20, 14, 2, 3, '#141020')
  b(10, 14, 1, 3, '#201a38'); b(20, 14, 1, 3, '#201a38')
  b(9, 16, 4, 1, '#1a1630'); b(19, 16, 4, 1, '#1a1630')
  drawTableCardGlow(ctx, tx, ty, phase)
  ctx.save()
  ctx.globalAlpha = 0.35
  b(5, 12, 5, 1, '#000000')
  b(13, 12, 6, 1, '#000000')
  b(24, 12, 5, 1, '#000000')
  ctx.restore()
  b(4, 7, 5, 5, '#110920')
  b(5, 8, 3, 3, '#3a1e68')
  p(6, 9, '#6838a8')
  b(12, 6, 6, 6, '#110920')
  b(13, 7, 4, 4, '#d0c0a0')
  p(15, 8, '#9060c0')
  p(14, 9, '#9060c0'); p(15, 9, '#9060c0'); p(16, 9, '#9060c0')
  p(15, 10, '#9060c0')
  b(23, 7, 5, 5, '#110920')
  b(24, 8, 3, 3, '#3a1e68')
  p(25, 9, '#6838a8')
}
